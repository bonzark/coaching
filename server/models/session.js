const mongoose = require("mongoose");

const sessionSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  time: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  isFirstSessionFree: {
    type: Boolean,
    default: true,
  },
});

const Session = mongoose.model("Session", sessionSchema);
module.exports = Session;
